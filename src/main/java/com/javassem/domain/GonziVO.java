package com.javassem.domain;

public class GonziVO {

	private int gonziNo;
	private String gonziName;
	private String gonziDetail;

	public int getGonziNo() {
		return gonziNo;
	}
	public void setGonziNo(int gonziNo) {
		this.gonziNo = gonziNo;
	}
	public String getGonziName() {
		return gonziName;
	}
	public void setGonziName(String gonziName) {
		this.gonziName = gonziName;
	}
	public String getGonziDetail() {
		return gonziDetail;
	}
	public void setGonziDetail(String gonziDetail) {
		this.gonziDetail = gonziDetail;
	}
}
