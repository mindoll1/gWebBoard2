package com.javassem.domain;

public class AtmonVO {

	private int moonNo;
	private String moonTitle;
	private String moonDetail;
	
	
	public int getMoonNo() {
		return moonNo;
	}
	public void setMoonNo(int moonNo) {
		this.moonNo = moonNo;
	}
	public String getMoonTitle() {
		return moonTitle;
	}
	public void setMoonTitle(String moonTitle) {
		this.moonTitle = moonTitle;
	}
	public String getMoonDetail() {
		return moonDetail;
	}
	public void setMoonDetail(String moonDetail) {
		this.moonDetail = moonDetail;
	}
	
}
